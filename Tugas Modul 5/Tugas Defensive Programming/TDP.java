public class TDP {
    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int numerator = 3;
        int denominator = 0;

        if (denominator == 0) {
            System.out.println("Error: Pembagian dengan nol tidak diperbolehkan.");
        } else {
            System.out.println(numerator / denominator);
        }
    }
}
